    INVALID_CONFIG = 3
    ROOT_REQUIRED = 4
    RESOURCE_LIMIT_FAILED = 5
    DUP2_FAILED = 6
    SETUID_FAILED = 7
    EXEC_FAILED = 8
    FORK_FAILED = 9
    WAIT_FAILED = 10
