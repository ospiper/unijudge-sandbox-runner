    SUCCESS = 0
    WRONG_ANSWER = 1
    CPU_TIME_LIMIT_EXCEEDED = 2
    REAL_TIME_LIMIT_EXCEEDED = 3
    MEMORY_LIMIT_EXCEEDED = 4
    RUNTIME_ERROR = 5
    COMPILE_ERROR = 6
    SYSTEM_ERROR = -1


